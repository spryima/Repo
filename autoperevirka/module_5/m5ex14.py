# Завдання буде схоже на попереднє, але тепер у тексті ми шукатимемо номер телефону України в міжнародному форматі, 
# шаблон якого наступний: +380(67)777-7-777 або +380(67)777-77-77

# Напишіть регулярний вираз для функції find_all_phones, яка буде в тексті (параметр text) знаходити всі телефонні 
# номери за вказаним шаблоном та повертати список отриманих з тексту збігів.

# З метою спрощення приймемо, що:

# використовуємо тільки цифри та символи +, (, ) та -
# телефонний номер починається із символу +
# шаблон телефону символ + потім три цифри 380, символ (, дві цифри, символ ), три цифри, символ -, одна або дві цифри, 
# символ -, дві чи три цифри
# Довжина шаблону телефонного номера завжди 17 символів
# Даний регулярний вираз жодною мірою не претендує на покриття всіх можливих варіантів телефонних номерів.

# При виконанні цього завдання ми рекомендуємо використовувати наступний сервіс для перевірок регулярних виразів regex101.


import re


def find_all_phones(text):
    result = re.findall(r"[+]380[(]\d\d[)]\d{3}-\d[\d-][\d-]\d\d", text)
    return result